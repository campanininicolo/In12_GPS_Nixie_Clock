void add_valid_line()
{
  write_pointer++;
  if (write_pointer >= LINE_BUFFER_SIZE) {
    write_pointer = 0;
  }
  if (valid_lines < LINE_BUFFER_SIZE) {
    valid_lines++;
  }
}




void remove_valid_line()
{
  read_pointer++;
  if (read_pointer >= LINE_BUFFER_SIZE) {
    read_pointer = 0;
  }
  if (valid_lines > 0) {
    valid_lines--;
  }
}
